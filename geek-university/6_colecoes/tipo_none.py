"""
tipo none

o tipo de dado None em python representa o tipo sem tipo , ou poderia ser conhecido também
como tipo vazio,porem , falar que é um tipo sem tipo e mas apropriado

obs: o tipo None é sempre especificado com a primeira letra maiuscula.

Certo: None
Errado: none

Quando utilizamos ?
    - Podemos utilizar None quando queremos crir uma variavel e inicializá-la com um tipo sem tipo, antes de recebermos um valor final.
"""
